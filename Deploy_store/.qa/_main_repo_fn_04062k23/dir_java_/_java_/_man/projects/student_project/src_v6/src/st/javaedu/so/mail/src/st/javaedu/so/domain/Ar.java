package st.javaedu.so.domain;

// Ar === address

// pC === postCode
// st === street
// bd === billing
// et === extension
// ap === apartment

public class Ar {

    private String pC_Ar;
    private String st_Ar;
    private String bd_Ar;
    private String et_Ar;
    private String ap_Ar;

    public String getpC_Ar() {
        return pC_Ar;
    }

    public void setpC_Ar(String pC_Ar) {
        this.pC_Ar = pC_Ar;
    }

    public String getSt_Ar() {
        return st_Ar;
    }

    public void setSt_Ar(String st_Ar) {
        this.st_Ar = st_Ar;
    }

    public String getBd_Ar() {
        return bd_Ar;
    }

    public void setBd_Ar(String bd_Ar) {
        this.bd_Ar = bd_Ar;
    }

    public String getEt_Ar() {
        return et_Ar;
    }

    public void setEt_Ar(String et_Ar) {
        this.et_Ar = et_Ar;
    }

    public String getAp_Ar() {
        return ap_Ar;
    }

    public void setAp_Ar(String ap_Ar) {
        this.ap_Ar = ap_Ar;
    }
}
