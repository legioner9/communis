// Поразрядные логические операции C++

#include <iostream>
using namespace std;
 
int main(int argc, char* argv[])
{
       cout << "\n 15 & 9 = " << (15 & 9) << endl;
       cout << "\n 15 ^ 9 = " << (15 ^ 9) << endl;
       cout << "\n 15 | 9 = " << (15 | 9) << endl;

       cout << "\n 774 | 54 = " << (774 | 54) << endl;
       
    //    system("pause");
       return 0;
}